//! # ethexe-runtime
//!
//! A `no_std` WASM artifact that serves as the execution runtime for Gear programs in the ethexe
//! layer. Validators in `ethexe-processor` load this blob into Wasmtime and drive it through two
//! C-ABI entry points: one to validate and instrument a program code blob, and one to drain a
//! program message queue for an ethexe block.
//!
//! This crate is the thin WASM shell; the real logic lives in `ethexe-runtime-common`, which it
//! wires to host-provided storage, lazy-page, and promise syscalls. `ethexe-processor` is the only
//! crate that depends on `ethexe-runtime` directly — other ethexe crates depend on
//! `ethexe-runtime-common` instead.
//!
//! ## Public API
//!
//! Under `std` the crate exposes two build-generated byte-slice constants:
//!
//! | Item | Purpose |
//! |------|---------|
//! | `WASM_BINARY` | The optimized compiled blob. |
//! | `WASM_BINARY_BLOATY` | The unoptimized blob; this is what `ethexe-processor` embeds. |
//!
//! ## Entry Points
//!
//! When the blob runs inside Wasmtime it exports two C-ABI symbols (on `target_arch = "wasm32"`).
//! Each receives a `(ptr, len)` pair and returns a packed `(ptr, len)` `i64` that the host decodes
//! to read SCALE-encoded output:
//!
//! - `instrument_code(code_ptr, code_len) -> i64` — validate and instrument a code blob with gas
//!   metering, stack-height limits, and the Eth syscall set; output is the instrumented code and
//!   its metadata.
//! - `run(arg_ptr, arg_len) -> i64` — execute a block's program message queue; output is the
//!   chunked journal results together with gas spent.

#![cfg_attr(not(feature = "std"), no_std)]

#[cfg(feature = "std")]
include!(concat!(env!("OUT_DIR"), "/wasm_binary.rs"));

extern crate alloc;

mod wasm {
    use interface::{allocator_ri::RuntimeAllocator, logging_ri::RuntimeLogger};

    mod api;
    mod interface;
    mod storage;

    #[cfg_attr(target_arch = "wasm32", global_allocator)]
    #[cfg_attr(not(target_arch = "wasm32"), allow(unused))]
    static ALLOCATOR: RuntimeAllocator = RuntimeAllocator;

    #[cfg(target_arch = "wasm32")]
    #[unsafe(no_mangle)]
    extern "C" fn _start() {
        __start()
    }

    #[cfg_attr(not(target_arch = "wasm32"), allow(unused))]
    fn __start() {
        RuntimeLogger::init();
    }

    #[cfg(target_arch = "wasm32")]
    #[panic_handler]
    fn panic_handler(info: &core::panic::PanicInfo) -> ! {
        log::error!("{info}");
        core::arch::wasm32::unreachable()
    }
}
