//! # ethexe-runtime
//!
//! A `no_std` WASM artifact that serves as the execution runtime for Gear programs in the ethexe
//! layer. Validators in `ethexe-processor` load this blob into Wasmtime and drive it through two
//! C-ABI entry points.
//!
//! ## Responsibilities
//!
//! This crate has two concerns, both confined to a private `wasm` module:
//!
//! 1. **Code instrumentation** — validate and instrument a Gear program WASM blob with gas metering,
//!    stack-height limits, and the `SyscallKind::Eth` syscall set.
//! 2. **Queue execution** — drain a program message queue for one ethexe block and produce
//!    SCALE-encoded journals together with gas spent.
//!
//! All real logic lives in `ethexe-runtime-common`; this crate is the thin WASM shell that wires
//! that logic to host-provided storage, lazy-page, and promise syscalls, and exports the entry
//! points.
//!
//! ## Role in the Stack
//!
//! ```text
//! ethexe-processor
//!     │  embeds WASM_BINARY_BLOATY, instantiates blob in Wasmtime
//!     ▼
//! ethexe-runtime  (this crate — WASM artifact)
//!     │  calls
//!     ▼
//! ethexe-runtime-common
//!     │  process_queue / RuntimeInterface / ProgramState
//!     ▼
//! gear-core / gear-lazy-pages-interface
//! ```
//!
//! `ethexe-processor` is the only crate that depends on `ethexe-runtime` directly. Other ethexe
//! crates depend on `ethexe-runtime-common` instead.
//!
//! ## Entry Points
//!
//! Under `std` the crate exposes only two build-generated byte-slice constants (via
//! `include!(concat!(env!("OUT_DIR"), "/wasm_binary.rs"))`):
//!
//! - `WASM_BINARY` — the optimized compiled blob.
//! - `WASM_BINARY_BLOATY` — the unoptimized blob; this is what `ethexe-processor` embeds.
//!
//! When the blob runs inside Wasmtime the following C-ABI symbols are exported (only on
//! `target_arch = "wasm32"`):
//!
//! - `instrument_code(code_ptr: i32, code_len: i32) -> i64` — validate and instrument a code blob;
//!   returns a packed `(ptr, len)` pair pointing at SCALE-encoded output.
//! - `run(arg_ptr: i32, arg_len: i32) -> i64` — execute a block's program message queue; returns a
//!   packed `(ptr, len)` pair pointing at SCALE-encoded `(Vec<chunked journal results>, gas_spent)`.
//!
//! The packed return value is produced by `pack_u32_to_i64` (from `ethexe-runtime-common`). The
//! host is responsible for decoding it after the call returns.
//!
//! ## Key Types
//!
//! The following types are wired together inside the private `wasm` module:
//!
//! - `NativeRuntimeInterface` (`wasm::storage`) — implements `ethexe_runtime_common::RuntimeInterface`
//!   and `Storage`; routes program-state, lazy-page, and promise reads/writes to host syscalls.
//! - `RuntimeAllocator` (`wasm::interface::allocator_ri`) — global allocator active on `wasm32`.
//! - `RuntimeLogger` (`wasm::interface::logging_ri`) — log facade initialised by `_start()`.
//!
//! The input/output types for `run` (`ProcessQueueContext`, `ProgramJournals`) and the output of
//! `instrument_code` (`InstrumentedCode`, `CodeMetadata`) are re-used from `ethexe-runtime-common`
//! and `gear-core` respectively.
//!
//! ## Invariants
//!
//! - **ABI contract**: entry points receive a `(ptr, len)` pair and return a packed `i64` via
//!   `pack_u32_to_i64`. The encoded output buffer is leaked with `Box::leak` so the host can read
//!   it across the FFI boundary after the call returns.
//! - **Chunked journals**: the `run` output is split into 32 MiB chunks before encoding to stay
//!   within the WASM allocator limit.
//! - **Code-length guard**: `instrument_code` rejects blobs that exceed `schedule.limits.code_len`
//!   both before and after instrumentation.
//! - **Zero address shortcut**: `NativeRuntimeInterface::program_state(H256::zero())` returns
//!   `ProgramState::zero()` without a database read.
//! - **Host syscall stubs**: the `declare!` macro generates `unimplemented!` stubs for non-wasm32
//!   targets so the crate compiles for testing without real host functions.
//! - **Fixed version**: instrumentation stamps `ethexe_runtime_common::VERSION` (= 1) into the
//!   resulting `InstrumentedCode`. Upgrade handling is not yet implemented.

#![cfg_attr(not(feature = "std"), no_std)]

#[cfg(feature = "std")]
include!(concat!(env!("OUT_DIR"), "/wasm_binary.rs"));

extern crate alloc;

mod wasm {
    use interface::{allocator_ri::RuntimeAllocator, logging_ri::RuntimeLogger};

    mod api;
    mod interface;
    mod storage;

    #[cfg_attr(target_arch = "wasm32", global_allocator)]
    #[cfg_attr(not(target_arch = "wasm32"), allow(unused))]
    static ALLOCATOR: RuntimeAllocator = RuntimeAllocator;

    #[cfg(target_arch = "wasm32")]
    #[unsafe(no_mangle)]
    extern "C" fn _start() {
        __start()
    }

    #[cfg_attr(not(target_arch = "wasm32"), allow(unused))]
    fn __start() {
        RuntimeLogger::init();
    }

    #[cfg(target_arch = "wasm32")]
    #[panic_handler]
    fn panic_handler(info: &core::panic::PanicInfo) -> ! {
        log::error!("{info}");
        core::arch::wasm32::unreachable()
    }
}
